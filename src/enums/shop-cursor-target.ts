/**
 * Determines the cursor target when entering the shop phase.
 */
export enum ShopCursorTarget {
  /** Cursor points to Reroll */
  REROLL = "REROLL",
  /** Cursor points to Items */
  ITEMS = "ITEMS",
  /** Cursor points to Shop */
  SHOP = "SHOP",
  /** Cursor points to Check Team */
  CHECK_TEAM = "CHECK_TEAM",
}
